            <h4>It’s the closest thing to Toyota having a Ford Ranger Raptor rival in Europe.</h4>

            <p>The Toyota Hilux is already one of the most capable trucks in its segment, but the folks at Arctic Trucks
                keep finding ways to make it even more competent off the beaten path. They’ve been selling upgraded
                Hilux pickups for years, and you might remember the one that starred in the golden era of&nbsp;<a
                    href="https://www.motor1.com/tag/top-gear/" data-inline-widget="internal-links" data-type-id="7"
                    data-params="%7B%22tag%22%3A%22Top%20Gear%22%2C%22originalTitle%22%3A%22top%20gear%22%2C%22alias%22%3A%22top-gear%22%7D">Top
                    Gear</a>&nbsp;when it became the first passenger vehicle to cross Antarctica.</p>
            <p><a href="https://www.motor1.com/news/409265/arctic-trucks-ford-f150-at44/"
                    data-inline-widget="internal-links" data-type-id="0"
                    data-params="%7B%22article_edition_id%22%3A%22409265%22%2C%22section%22%3A%221%22%2C%22alias%22%3A%22arctic-trucks-ford-f150-at44%22%7D">Arctic
                    Trucks</a>&nbsp;has now worked on the latest version of the Hilux, which debuted back in early June
                last year. The Toyota-backed AT35 model starts off as a 2.8-liter double cab model in the suitably
                called Invincible X trim level and then it goes through several changes to beef up the truck’s off-road
                credentials. For example, the ground clearance increases by 65 millimeters (2.6 inches) while approach
                and departure angles jump by nine and three degrees, respectively.</p>
            <p>These modifications were possible after tweaking the Hilux’s frame and inner arches, while the bodywork
                also had to go through some changes. Arctic Trucks then fitted its own set of 17-inch wheels with chunky
                35-inch BFGoodrich KO2 All-Terrain tires to further enhance the reputable pickup’s go-anywhere
                capabilities.</p>
            <p>There’s more than meets the eye as the AT35 variant also has an upgraded Bilstein suspension with custom
                springs and dampers at the front, along with a revised anti-roll bar and modified rear dampers with
                extended shackles. Arctic Trucks’ Hilux has an additional 40 mm (1.6 in) front and 20 mm (0.8 in) of
                suspension lift compared to the standard Toyota variant. In addition, the front and rear differentials
                have been modified for better torque delivery.</p>
            <p>The Hilux AT35 can be further distinguished from the original Toyota model courtesy of a rear height bar
                with a built-in receiver hitch. Arctic Trucks also throws in some anniversary decals to celebrate 30
                years since the company was founded, and there’s also a flare package for the front and rear wings.
                Rounding off the upgrades are the extended side steps and even a certificate of authenticity.</p>
            <div class="item m1-ap-native "></div>
            <p>Power comes from the standard-fit four-cylinder 2.8-liter turbodiesel engine with 201 horsepower and an
                ample 500 Newton-meters (369 pound-feet) of torque from 1,600 rpm. There have been rumors of a&nbsp;<a
