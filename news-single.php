                    version</a>&nbsp;developed by Gazoo Racing to take on the Ford Ranger Raptor, but we’ll believe it
                when we’ll see it. In the meantime, Toyota’s New Zealand division has developed its own rugged Hilux,
                dubbed&nbsp;<a href="https://www.motor1.com/news/446655/toyota-hilux-mako-ranger-raptor-fighter/"
